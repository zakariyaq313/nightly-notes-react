import React from "react";
import FontSelect from "../FontSelect/FontSelect";
import Palette from "../Palette/Palette";

type ButtonPanelProps = {
  theme: {
    font: boolean,
    palette: boolean
  },

  onUpdateTheme: (value: {font: boolean, palette: boolean}) => void,
  onUploadImage: () => void
};

function ButtonPanel(props: ButtonPanelProps) {
  const {theme, onUpdateTheme, onUploadImage} = props;
  
  const openFontSelect = () => {
    onUpdateTheme({
      font: !theme.font,
      palette: false
    });
  }

  const openPalette = (e: React.FormEvent<HTMLButtonElement>) => {
    e.preventDefault();
    onUpdateTheme({
      font: false,
      palette: !theme.palette
    });
  }

  const closeElements = () => {
    onUpdateTheme({
      font: false,
      palette: false
    });
  }

  const uploadImage = (e: React.FormEvent<HTMLButtonElement>) => {
    e.preventDefault();
    closeElements();
    onUploadImage();
  }

  return(
		<div className="note-options">
      <span onClick={openFontSelect} className="font-select-button">
        Font family
        <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24"><path d="M0 0h24v24H0z" fill="none"/><path d="M16.59 8.59L12 13.17 7.41 8.59 6 10l6 6 6-6z"/></svg>
      </span>

      <button onClick={uploadImage} title="Image">
        <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24"><path d="M0 0h24v24H0V0z" fill="none"/><path d="M19 5v14H5V5h14m0-2H5c-1.1 0-2 .9-2 2v14c0 1.1.9 2 2 2h14c1.1 0 2-.9 2-2V5c0-1.1-.9-2-2-2zm-4.86 8.86l-3 3.87L9 13.14 6 17h12l-3.86-5.14z"/></svg>
      </button>

      <button onClick={openPalette} title="Background colour">
        <svg xmlns="http://www.w3.org/2000/svg" enableBackground="new 0 0 24 24" viewBox="0 0 24 24"><g><rect fill="none" height="24" width="24"/></g><g><g><g><g><path d="M12,22C6.49,22,2,17.51,2,12S6.49,2,12,2s10,4.04,10,9c0,3.31-2.69,6-6,6h-1.77c-0.28,0-0.5,0.22-0.5,0.5 c0,0.12,0.05,0.23,0.13,0.33c0.41,0.47,0.64,1.06,0.64,1.67C14.5,20.88,13.38,22,12,22z M12,4c-4.41,0-8,3.59-8,8s3.59,8,8,8 c0.28,0,0.5-0.22,0.5-0.5c0-0.16-0.08-0.28-0.14-0.35c-0.41-0.46-0.63-1.05-0.63-1.65c0-1.38,1.12-2.5,2.5-2.5H16 c2.21,0,4-1.79,4-4C20,7.14,16.41,4,12,4z"/><circle cx="6.5" cy="11.5" r="1.5"/><circle cx="9.5" cy="7.5" r="1.5"/><circle cx="14.5" cy="7.5" r="1.5"/><circle cx="17.5" cy="11.5" r="1.5"/></g></g></g></g></svg>
      </button>

      <button  disabled title="Delete">
        <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24"><path d="M0 0h24v24H0V0z" fill="none"/><path d="M16 9v10H8V9h8m-1.5-6h-5l-1 1H5v2h14V4h-3.5l-1-1zM18 7H6v12c0 1.1.9 2 2 2h8c1.1 0 2-.9 2-2V7z"/></svg>
      </button>

      <button v-if="trashIsOpen" title="Delete forever">
        <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24"><path d="M0 0h24v24H0V0z" fill="none"/><path d="M16 9v10H8V9h8m-1.5-6h-5l-1 1H5v2h14V4h-3.5l-1-1zM18 7H6v12c0 1.1.9 2 2 2h8c1.1 0 2-.9 2-2V7z"/></svg>
      </button>

      <button v-if="trashIsOpen" title="Restore">
        <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24"><path d="M0 0h24v24H0z" fill="none"/><path d="M13 3c-4.97 0-9 4.03-9 9H1l3.89 3.89.07.14L9 12H6c0-3.87 3.13-7 7-7s7 3.13 7 7-3.13 7-7 7c-1.93 0-3.68-.79-4.94-2.06l-1.42 1.42C8.27 19.99 10.51 21 13 21c4.97 0 9-4.03 9-9s-4.03-9-9-9zm-1 5v5l4.28 2.54.72-1.21-3.5-2.08V8H12z"/></svg>
      </button>

      { theme.font && <FontSelect /> }
      { theme.palette && <Palette /> }
    </div>
  );
}

export default ButtonPanel;
